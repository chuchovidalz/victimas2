                    <h3>Introducción</h3>
                    <img src="img/intro.jpg" width="250">
                    <p>Si se pretende trabajar con adolescentes y adultos jóvenes, se tiene que tener en cuenta que existen diferentes desafíos y oportunidades que son únicas para cada uno de ellos, ya que le problemas de caer en conductas de riesgo tal como, sexo sin protección, y uso de substancias psicoactivas que comienzas en la adolescencia y prevalecen en la adultez. (Park, Muyle, Adams, Brindis, &Irwin, 2006)</p>
                    <p>Pobres herramientas para resolver problemas de la vida diaria pueden causar diferentes problemas de salud a lo largo de la vida. Lo que puede causar conflictos con los que lo rodean y presión de los  pares, contribuyen a aumentar el estrés en este tipo de personas, por lo que si se pretende trabajar con adultos resistentes al cambio presenta un gran reto ya que cada usuario  presenta diferentes tipos de dificultades y se tienen que balancear muchos comportamientos tal como factores socioculturales. Para poder trabajar con ellos es  una tarea monumental para poder alcanzar  cambios de comportamiento más positivos. </p>
                    <p>Para poder abordar este tipo de situaciones  una técnica llamada Entrevista Motivacional que es efectiva para el cambio de comportamiento usada en adultos y pacientes resistentes,  la cual puede ser aplicada en una gran variedad problemas conductuales de la vida cotidiana ya que es buena lidiando con la ambivalencia y la resistencia al cambio.</p>
                    <p>La teoría de Miller y Rollnick también se inspira en el trabajo de Carl Rogers en el asesoramiento no directivo, que se describe en 1953.  La resolución de la ambivalencia es el propósito central de la orientación no directiva de la entrevista motivacional.</p>
                    <br>
                    
                    <li id="objetivos"  class="subtema"><h4>OBJETIVOS:</h4></li>
                        <ul>
                        <li>El objetivo prioritario y fundamental del psicólogo clínico es incrementar el bienestar de un paciente, ayudándole a eliminar los distintos problemas que contribuyen a su desajuste social, o reducir la emisión de conductas desadaptadas, sentimientos negativos, o pensamientos distorsionados.</li>
                        <li>Al  concluir el  curso, el participante conocerá los conceptos básicos de la entrevista motivacional, para su aplicación en el contexto de salud mental en la aplicación de problemas laborales, en la cual usara diferentes tipos de habilidades clínicas para la entrevista clínica inicial, vía internet en el enfoque Cognitivo-Conductual.</li>
                        <li>La parte final del curso se dedicará a la práctica simulada de los participantes, a fin de evaluar sus habilidades clínicas adquiridas durante el curso, en donde se pretende que incorporen los conocimientos teóricos en una práctica supervisada.</li>
                        </ul>
